// #### **Задача 8: Функция-утилита для массива (Указатели и размер)**
// Напишите **одну** универсальную функцию с именем `arrayInfo`, которая принимает указатель на массив целых чисел и его размер, и выводит на экран:
// *   Размер массива.
// *   Сумму элементов.
// *   Первый и последний элемент.

// Продемонстрируйте работу этой функции на двух разных массивах разной длины.

// **Цель:** Закрепление практики передачи массивов в функции через указатель и размер.

#include <iostream>

// Универсальная функция для работы с массивом
void arrayInfo(const int* arr, int size) {
    if (size <= 0) {
        std::cout << "Массив пуст." << std::endl;
        return;
    }

    std::cout << "Размер массива: " << size << std::endl;

    int sum = 0;
    for (int i = 0; i < size; ++i) {
        sum += arr[i];
    }
    std::cout << "Сумма элементов: " << sum << std::endl;

    std::cout << "Первый элемент: " << arr[0] << std::endl;
    std::cout << "Последний элемент: " << arr[size - 1] << std::endl;
}

int main() {
    int arr1[] = {10, 20, 30, 40, 50};
    int size1 = sizeof(arr1) / sizeof(arr1[0]);

    int arr2[] = {5, 15, 25, 35};
    int size2 = sizeof(arr2) / sizeof(arr2[0]);

    std::cout << "Информация о первом массиве:" << std::endl;
    arrayInfo(arr1, size1);

    std::cout << "\nИнформация о втором массиве:" << std::endl;
    arrayInfo(arr2, size2);

    return 0;
}
